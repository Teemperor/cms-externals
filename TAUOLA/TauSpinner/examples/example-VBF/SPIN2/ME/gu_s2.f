      INTEGER I3,I4,H1,H2
      ANS=0.D0
C     GLOBAL VARIABLES
C     
         PP(0:3,1) = P(0:3,1)
         PP(0:3,2) = P(0:3,2)
         PP(0:3,5) = P(0:3,5)
         PP(0:3,6) = P(0:3,6)

         PP(0:3,3) = P(0:3,4)
         PP(0:3,4) = P(0:3,3)
C     ----------
C     BEGIN CODE
C     ----------
      IF(I3.EQ.21 .AND. I4.EQ.2) CALL gu_gu_s2(P,H1,H2,ANS)
      If(I3.EQ.21 .AND. I4.EQ.1) CALL gu_gu_s2(P,H1,H2,ANS)
      IF(I3.EQ.2 .AND. I4.EQ.21) CALL gu_gu_s2(PP,H1,H2,ANS)
      If(I3.EQ.1 .AND. I4.EQ.21) CALL gu_gu_s2(PP,H1,H2,ANS)
      END

