      SUBROUTINE ddx_s2(P,I3,I4,H1,H2,ANS)
      IMPLICIT NONE
C     
C     CONSTANT
C     
      INTEGER    NEXTERNAL
      PARAMETER (NEXTERNAL=6)
      INTEGER                 NCOMB
      PARAMETER (             NCOMB=64)

C     
C     ARGUMENTS 
C     
      REAL*8 P(0:3,NEXTERNAL),ANS
      INTEGER I3,I4,H1,H2
      REAL*8 PP(0:3,6)
C     GLOBAL VARIABLES
C     
C     ----------
C     BEGIN CODE
C     ----------
      ANS=0.D0

      ! switch 4-vectors for I3, I4 and provide/(add contribution from) new configuration
      PP(0:3,1) = P(0:3,1)
      PP(0:3,2) = P(0:3,2)
      PP(0:3,5) = P(0:3,5)
      PP(0:3,6) = P(0:3,6)
      
      PP(0:3,3) = P(0:3,4)
      PP(0:3,4) = P(0:3,3)
C     
C     GLOBAL VARIABLES
C     
C     ----------
C     BEGIN CODE
C     ----------
      IF(I3.EQ.4 .AND. I4.EQ.-4) CALL ddx_ccx_s2(P,H1,H2,ANS)
      IF(I3.EQ.4 .AND. I4.EQ.-2) CALL ddx_cux_s2(P,H1,H2,ANS)
      IF(I3.EQ.1 .AND. I4.EQ.-1) CALL ddx_ddx_s2(P,H1,H2,ANS)
      IF(I3.EQ.3 .AND. I4.EQ.-3) CALL ddx_ssx_s2(P,H1,H2,ANS)
      IF(I3.EQ.2 .AND. I4.EQ.-4) CALL ddx_ucx_s2(P,H1,H2,ANS)
      IF(I3.EQ.2 .AND. I4.EQ.-2) CALL ddx_uux_s2(P,H1,H2,ANS)
      IF(I3.EQ.21.AND.I4.EQ.21)  CALL ddx_gg_s2(P,H1,H2,ANS)
      IF(I3.EQ.-4 .AND. I4.EQ.4) CALL ddx_ccx_s2(PP,H1,H2,ANS)
      IF(I3.EQ.-2 .AND. I4.EQ.4) CALL ddx_cux_s2(PP,H1,H2,ANS)
      IF(I3.EQ.-1 .AND. I4.EQ.1) CALL ddx_ddx_s2(PP,H1,H2,ANS)
      IF(I3.EQ.-3 .AND. I4.EQ.3) CALL ddx_ssx_s2(PP,H1,H2,ANS)
      IF(I3.EQ.-4 .AND. I4.EQ.2) CALL ddx_ucx_s2(PP,H1,H2,ANS)
      IF(I3.EQ.-2 .AND. I4.EQ.2) CALL ddx_uux_s2(PP,H1,H2,ANS)


      END
      
