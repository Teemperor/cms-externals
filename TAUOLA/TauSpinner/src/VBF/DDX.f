                ANS=ANS1+ANS2+ANS3+ANS4+ANS5+ANS6+ANS
                CALL DDX_GG_NOH(P,H1,H2,ANS1)
                ANS=ANS1+ANS
            ENDIF   
      ELSE
           WRITE(*,*) 'NOT FINISHED'
           STOP
      ENDIF
      END SUBROUTINE DDX



