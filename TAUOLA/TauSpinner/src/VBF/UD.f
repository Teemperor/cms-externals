      SUBROUTINE  UD(P,I3,I4,H1,H2,KEY,ANS)
      IMPLICIT NONE
 
      INTEGER I3,I4,   H1,H2,   KEY
      REAL*8 P(0:3,6), ANS, ANS1,ANS2,ANS3,ANS4
      REAL*8  PP(0:3,6)

      ANS=0.D0
C      I3=0,2 OR 4             I4=0,1 OR 3

      ! switch 4-vectors for I3, I4 and provide/(add contribution from) new configuration
      PP(0:3,1) = P(0:3,1)
      PP(0:3,2) = P(0:3,2)
      PP(0:3,5) = P(0:3,5)
      PP(0:3,6) = P(0:3,6)
      
      PP(0:3,3) = P(0:3,4)
      PP(0:3,4) = P(0:3,3)

      IF(KEY.EQ.1) THEN
         IF(I3.EQ.2 .AND. I4.EQ.1) CALL UD_UD_H(P,H1,H2,ANS)
         IF(I3.EQ.2 .AND. I4.EQ.3) CALL UD_US_H(P,H1,H2,ANS)
         IF(I3.EQ.4 .AND. I4.EQ.1) CAll UD_CD_H(P,H1,H2,ANS)
         IF(I3.EQ.4 .AND. I4.EQ.3) CALL UD_CS_H(P,H1,H2,ANS)

         IF(I3.EQ.1 .AND. I4.EQ.2) CALL UD_UD_H(PP,H1,H2,ANS)
         IF(I3.EQ.3 .AND. I4.EQ.2) CALL UD_US_H(PP,H1,H2,ANS)
         IF(I3.EQ.1 .AND. I4.EQ.4) CAll UD_CD_H(PP,H1,H2,ANS)
         IF(I3.EQ.3 .AND. I4.EQ.4) CALL UD_CS_H(PP,H1,H2,ANS)
            IF(I3.EQ.0 .AND. I4.EQ.0) THEN 
                CALL UD_UD_H(P,H1,H2,ANS1)
                CALL UD_US_H(P,H1,H2,ANS2)
                CALL UD_CD_H(P,H1,H2,ANS3)
                CALL UD_CS_H(P,H1,H2,ANS4)
                ANS=ANS1+ANS2+ANS3+ANS4
                CALL UD_UD_H(PP,H1,H2,ANS1)
                CALL UD_US_H(PP,H1,H2,ANS2)
                CALL UD_CD_H(PP,H1,H2,ANS3)
                CALL UD_CS_H(PP,H1,H2,ANS4)
                ANS=ANS1+ANS2+ANS3+ANS4+ANS
            ENDIF
      ELSE IF(KEY.EQ.0) THEN 
         IF(I3.EQ.2 .AND. I4.EQ.1) CALL UD_UD_NOH(P,H1,H2,ANS)
         IF(I3.EQ.2 .AND. I4.EQ.3) CALL UD_US_NOH(P,H1,H2,ANS)
         IF(I3.EQ.4 .AND. I4.EQ.1) CAll UD_CD_NOH(P,H1,H2,ANS)
         IF(I3.EQ.4 .AND. I4.EQ.3) CALL UD_CS_NOH(P,H1,H2,ANS)

         IF(I3.EQ.1 .AND. I4.EQ.2) CALL UD_UD_NOH(PP,H1,H2,ANS)
         IF(I3.EQ.3 .AND. I4.EQ.2) CALL UD_US_NOH(PP,H1,H2,ANS)
         IF(I3.EQ.1 .AND. I4.EQ.4) CAll UD_CD_NOH(PP,H1,H2,ANS)
         IF(I3.EQ.3 .AND. I4.EQ.4) CALL UD_CS_NOH(PP,H1,H2,ANS)
            IF(I3.EQ.0 .AND. I4.EQ.0) THEN 
                CALL UD_UD_NOH(P,H1,H2,ANS1)
                CALL UD_US_NOH(P,H1,H2,ANS2)
                CALL UD_CD_NOH(P,H1,H2,ANS3)
                CALL UD_CS_NOH(P,H1,H2,ANS4)
                ANS=ANS1+ANS2+ANS3+ANS4
                CALL UD_UD_NOH(PP,H1,H2,ANS1)
                CALL UD_US_NOH(PP,H1,H2,ANS2)
                CALL UD_CD_NOH(PP,H1,H2,ANS3)
                CALL UD_CS_NOH(PP,H1,H2,ANS4)
                ANS=ANS1+ANS2+ANS3+ANS4+ANS
            ENDIF   
      ELSE
           WRITE(*,*) 'NOT FINISHED'
           STOP
      ENDIF
      END SUBROUTINE UD



